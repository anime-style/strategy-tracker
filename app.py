# --- Callbacks ---
# This single callback updates all three charts.
# It's triggered by the 'initial-load-trigger' Store component when the app loads.
@app.callback(
    [Output('mstr-mnav-chart', 'figure'),      # Output for the first chart
     Output('mnav-bollinger-chart', 'figure'), # Output for the second chart
     Output('btc-price-chart', 'figure')],     # Output for the third chart
    [Input('initial-load-trigger', 'data')]    # Input (dummy) to trigger on load
)
def update_all_charts(initial_trigger_data) -> tuple[go.Figure, go.Figure, go.Figure]:
    """
    Loads historical data and generates/updates all Plotly chart figures for the dashboard.

    This callback is triggered once when the app loads due to the 'initial-load-trigger'
    Store component. Its 'data' argument is not used but serves to initiate the callback.

    Args:
        initial_trigger_data: Data from the dcc.Store component. Its value is not
                              material for this callback, which acts as an on-load trigger.

    Returns:
        tuple[go.Figure, go.Figure, go.Figure]: A tuple containing the three Plotly
                                                figure objects for the charts.
                                                Returns empty figures if data loading fails.
    """
    logging.info("Dash app: Callback update_all_charts triggered.") # Clarified log source
    df = load_historical_data() # Load data from "mstr_historical_mnav.csv"

    # Define common layout properties for a consistent dark theme across charts
    dark_layout_props = {
        'template': 'plotly_dark',             # Use Plotly's dark theme as a base
        'paper_bgcolor': 'rgba(40,40,40,0.8)', # Background of the entire chart area
        'plot_bgcolor': 'rgba(20,20,20,0.8)',   # Background of the plotting area itself
        'font': {'color': '#FFFFFF'},          # Default font color for titles, axes, legends
        'legend_title_font_color': 'white',
        'xaxis_title_font_color': 'white',
        'yaxis_title_font_color': 'white',
        'hovermode': 'x unified'               # Unified tooltip for all traces at a given x-value
    }

    # Default empty figure to return in case of errors or no data
    empty_fig_layout = go.Layout(title_text="Data not available", **dark_layout_props)
    empty_fig = go.Figure(layout=empty_fig_layout)

    if df.empty:
        logging.warning("Dash app: Historical data is empty. Returning empty charts for all outputs.")
        return empty_fig, empty_fig, empty_fig

    # --- Chart 1: MSTR Price and MNAV ---
    fig1 = go.Figure()
    # Add MSTR closing price trace
    if 'Close_MSTR' in df.columns:
        fig1.add_trace(go.Scatter(x=df.index, y=df['Close_MSTR'], mode='lines', name='MSTR Price', line=dict(color='royalblue')))
    else:
        logging.warning("Dash app: Column 'Close_MSTR' not found in data for Chart 1.")
    # Add MNAV trace
    if 'MNAV' in df.columns:
        fig1.add_trace(go.Scatter(x=df.index, y=df['MNAV'], mode='lines', name='MNAV (USD)', line=dict(color='cyan')))
    else:
        logging.warning("Dash app: Column 'MNAV' not found in data for Chart 1.")
    # Update layout for Chart 1
    fig1.update_layout(
        title_text='MSTR Price vs. MNAV Over Time',
        xaxis_title='Date', yaxis_title='Value (USD)',
        legend_title_text='Legend',
        **dark_layout_props # Apply common dark theme
    )

    # --- Chart 2: MNAV with Bollinger Bands ---
    fig2 = go.Figure()
    if 'MNAV' in df.columns and not df['MNAV'].empty:
        df_bb = calculate_bollinger_bands(df['MNAV']) # Defaults: window=20, num_std_dev=2
        if not df_bb.empty:
            # Plot Bollinger Bands fill area first (semi-transparent)
            fig2.add_trace(go.Scatter(x=df_bb.index, y=df_bb['UpperBand'], mode='lines', line=dict(width=0.5, color='rgba(173,216,230,0.0)'), showlegend=False, name='Upper Bound Fill'))
            fig2.add_trace(go.Scatter(x=df_bb.index, y=df_bb['LowerBand'], mode='lines', line=dict(width=0.5, color='rgba(173,216,230,0.0)'), fill='tonexty', fillcolor='rgba(173,216,230,0.2)', showlegend=False, name='Lower Bound Fill'))

            fig2.add_trace(go.Scatter(x=df_bb.index, y=df_bb['Original'], mode='lines', name='MNAV', line=dict(color='blue')))
            fig2.add_trace(go.Scatter(x=df_bb.index, y=df_bb['SMA'], mode='lines', name='SMA (20-day)', line=dict(color='orange')))

            fig2.add_trace(go.Scatter(x=df_bb.index, y=df_bb['UpperBand'], mode='lines', line=dict(dash='dash', color='lightgrey'), name='Upper Band'))
            fig2.add_trace(go.Scatter(x=df_bb.index, y=df_bb['LowerBand'], mode='lines', line=dict(dash='dash', color='lightgrey'), name='Lower Band'))
        else:
            logging.warning("Dash app: Bollinger Bands calculation returned empty DataFrame for Chart 2.")
    else:
        logging.warning("Dash app: Column 'MNAV' not found or is empty; cannot generate Bollinger Bands chart.")
    # Update layout for Chart 2
    fig2.update_layout(
        title_text='MNAV with Bollinger Bands (20-day, 2 Std Dev)',
        xaxis_title='Date', yaxis_title='Value (USD)',
        legend_title_text='Legend',
        **dark_layout_props # Apply common dark theme
    )

    # --- Chart 3: Bitcoin Price ---
    fig3 = go.Figure()
    # Add BTC closing price trace
    if 'Close_BTC' in df.columns:
        fig3.add_trace(go.Scatter(x=df.index, y=df['Close_BTC'], mode='lines', name='BTC Price', line=dict(color='gold')))
    else:
        logging.warning("Dash app: Column 'Close_BTC' not found in data for Chart 3.")
    # Update layout for Chart 3
    fig3.update_layout(
        title_text='Bitcoin (BTC-USD) Price Over Time',
        xaxis_title='Date', yaxis_title='Price (USD)',
        legend_title_text='Legend',
        **dark_layout_props # Apply common dark theme
    )

    logging.info("Dash app: Successfully generated and styled all chart figures.")
    return fig1, fig2, fig3

