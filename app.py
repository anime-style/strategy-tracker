# --- Data Preparation Functions ---

def load_historical_data(csv_filepath: str = "mstr_historical_mnav.csv") -> pd.DataFrame:
    """
    Loads historical MSTR price, BTC price, and MNAV data from a CSV file.

    The CSV file is expected to have 'Date' as its first column, which will be
    used as the DataFrame index and parsed as datetime objects.

    Args:
        csv_filepath (str, optional): The path to the CSV file.
                                      Defaults to "mstr_historical_mnav.csv", assuming
                                      it's located in the same directory as app.py.

    Returns:
        pd.DataFrame: A pandas DataFrame with 'Date' as the index. Expected columns
                      include 'Close_MSTR', 'Close_BTC', and 'MNAV'.
                      Returns an empty DataFrame if the file is not found or any
                      other error occurs during loading.
    """
    try:
        # Read CSV, using 'Date' column as index and parsing it as datetime objects
        data = pd.read_csv(csv_filepath, index_col='Date', parse_dates=True)
        logging.info(f"Successfully loaded data from {csv_filepath} for Dash app.") # Clarified log
        # Basic validation: check for expected columns
        expected_cols = ['Close_MSTR', 'Close_BTC', 'MNAV']
        if not all(col in data.columns for col in expected_cols):
            logging.warning(f"Dash app: Loaded data from {csv_filepath} is missing one or more expected columns: {expected_cols}. Found columns: {list(data.columns)}")
        return data
    except FileNotFoundError:
        logging.error(f"Dash app: Error - The data file {csv_filepath} was not found.")
        return pd.DataFrame()
    except Exception as e:
        logging.error(f"Dash app: An error occurred while loading data from {csv_filepath}: {e}", exc_info=True)
        return pd.DataFrame()

def calculate_bollinger_bands(data_series: pd.Series, window: int = 20, num_std_dev: int = 2) -> pd.DataFrame:
    """
    Calculates Bollinger Bands (SMA, Upper Band, Lower Band) for a given pandas Series.

    Args:
        data_series (pd.Series): The input time series data (e.g., MNAV values).
        window (int, optional): The rolling window period for calculating the
                                Simple Moving Average (SMA) and standard deviation.
                                Defaults to 20.
        num_std_dev (int, optional): The number of standard deviations to use for
                                     the upper and lower bands. Defaults to 2.

    Returns:
        pd.DataFrame: A DataFrame with columns: 'Original' (the input data_series),
                      'SMA' (Simple Moving Average), 'UpperBand', and 'LowerBand'.
                      Returns an empty DataFrame if the input is not a valid Series,
                      is empty, or if other issues occur.
    """
    if not isinstance(data_series, pd.Series):
        logging.error("Dash app: Invalid input - data_series must be a pandas Series for Bollinger Bands calculation.")
        return pd.DataFrame()
    if data_series.empty:
        logging.warning("Dash app: Input data_series is empty. Cannot calculate Bollinger Bands.")
        return pd.DataFrame()
    if len(data_series) < window:
        logging.warning(f"Dash app: Data series length ({len(data_series)}) is less than window size ({window}). Bollinger Bands will have many NaNs initially.")

    df_bb = pd.DataFrame()
    df_bb['Original'] = data_series
    df_bb['SMA'] = data_series.rolling(window=window, min_periods=1).mean()
    rolling_std = data_series.rolling(window=window, min_periods=1).std()
    df_bb['UpperBand'] = df_bb['SMA'] + (rolling_std * num_std_dev)
    df_bb['LowerBand'] = df_bb['SMA'] - (rolling_std * num_std_dev)

    logging.info(f"Dash app: Successfully calculated Bollinger Bands for series with window {window} and {num_std_dev} std dev.")
    return df_bb

